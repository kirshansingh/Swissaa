                        <div class="col-md-8">
							<p>Hamish LAL, Brendan CASEY, Josephine KAIDING and Léa DEFRANCHI denounce the continued lack of coherence in the treatment of multi-tiered dispute resolution clauses in international arbitration and submit that institutional rules must be amended to grant arbitral tribunals the explicit power to decide on their jurisdiction and choose from the full array of options when dealing with the interpretation and enforceability of such clauses. (Hamish LAL, Brendan CASEY, Josephine KAIDING, Léa DEFRANCHI, Multi-Tiered Dispute Resolution Clauses in International Arbitration – The Need for Coherence).</p>

							<p>In his paper, Reto Andrea TETTAMANTI examines the question of the transitional law provisions applicable to the revision of Chapter 12 of the Swiss Private International Law Act (PILA), Switzerland’s lex arbitri, and shows how these provisions would apply to some of the most relevant changes arising from the revision. (Reto Andrea TETTAMANTI, Intertemporales Schiedsrecht. Die für die Revision des 12. Kapitels IPRG relevanten Übergangsbestimmungen).</p>

							<p>Simon BACHMANN assesses the impact of third-party funding on requests for security for costs in international arbitration proceedings seated in Switzerland and examines whether the approach under the Swiss lex arbitri is in line with best international arbitration practice (Simon BACHMANN, The Impact of Third-Party Funding on Security for Costs Requests in International Arbitration Proceedings in Switzerland. Why and how third-party funding should be considered under the Swiss lex arbitri).
							Giulio PALERMO and Panagiotis KYRIAKOU advocate in favor of the ex æquo et bono standard in international arbitration by outlining its advantages and exploring how the risks commonly associated with its application can be mitigated in light of the case law of the Basketball Arbitral Tribunal. (Giulio PALERMO, Panagiotis KYRIAKOU, Leveraging the Standard of Ex Aequo et Bono to Increase Diversity, Flexibility and Efficiency: Insights from the Basketball Arbitral Tribunal).</p>

							<p>Andreea NICA discusses the recent decision of the Swiss Federal Supreme Court rendered on 25 March 2020 in Clorox v. Venezuela (4A_306/2019), which is of particular interest as it is the first time an award rendered in an investment treaty arbitration seated in Switzerland is set aside. The Supreme Court concluded that the arbitral tribunal had wrongly denied jurisdiction for lack of an “action of investing” as it found that, in the absence of adequate language prohibiting treaty shopping in the relevant treaty, the investor’s nationality was the only threshold requirement. (Andreea NICA, Case Note on the Decision of the Swiss Federal Tribunal in Clorox v.Venezuela).</p>

							<p>Samantha NATAF reports on two recent decisions rendered by the English and French Courts in relation to the enforcement of the same award. These decisions highlight the divergent approaches of the two jurisdictions regarding the law applicable to international arbitration agreements and the resulting risk of contradictory decisions. (Samantha NATAF, Jurisdiction over Non-signatories, the Irreconcilable Approaches of French and English Courts. Case Note on: (i) English Court of Appeal Decision of 20 January 2020 and (ii) Paris Court of Appeal Decision of 23 June 2020).</p>

							<p>In his paper, Mahmoud Anis BETTAIEB discusses the important role played by the Tunisian judiciary in promoting arbitration as a private dispute resolution mode, be it in providing assistance for the constitution of the arbitral tribunal and its operation, or when interpreting and applying the Tunisian Code of Arbitration. (Mahmoud Anis BETTAIEB, Le juge tunisien et la promotion de l’arbitrage).</p>

							<p>Nadia SMAHI analyzes due process under the Swiss Rules of International Arbitration and provides an overview of all decisions rendered by the Swiss Federal Supreme Court in relation to Swiss Rules arbitrations seated in Switzerland since June 2012. (Nadia SMAHI, Due Process Under the Swiss Rules of International Arbitration).</p>
							
