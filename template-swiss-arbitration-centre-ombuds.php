							<p>On 1 January 2020, the Swiss Financial Services Act (FinSA) entered into force and clients of financial service providers can now avail themselves of an ombudsman/mediation procedure. For this purpose, the financial service providers active in Switzerland must be affiliated to an ombudsman recognised by the Swiss Federal Finance Department (FDF).</p>

							<p>SCAI has obtained the FDF recognition in July 2020 and is now offering these specific mediation services to all interested Financial Service Providers, client advisers, trustees, and clients thereof, whether subject to the FinSA or not, in accordance with the SCAI Rules of Mediation Procedure for Financial Services.</p>
							<p>Non-Swiss Financial Service Providers who are not active in Switzerland are not subject to the FinSA and do not have to affiliate themselves with a Swiss Mediation Organ such as SCAI. Non-Swiss Financial Service Providers can nonetheless agree with their non-Swiss clients to use our Mediation Organ either by inserting in their contracts a mediation clause referring to SCAI and the SCAI Rules of Mediation Procedure for Financial Services, or by agreeing with their clients to refer thereto once a dispute arises. Model Mediation Clauses are available here.</p>
