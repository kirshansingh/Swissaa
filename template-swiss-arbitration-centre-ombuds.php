							<p>If you decide to affiliate yourself with our ombudsman service / mediation organ, you must fill in the below form and pay the affiliation fees. We will then confirm your affiliation and you will have filled in your obligation to affilate yourself to an ombudsman office as per article 74 et seq. FinSA. You will benefit of our services right away if you face a dispute. The affiliation fee will cover the remaining of 2020 and 2021 in full.</p>
							
							<p>mid-November 2020 NEWS!</p>
							
							<p>The Swiss Parliament has recently decided a limitation on the ombudsman affiliation requirement. As of February 1, 2021, financial service providers that serve solely institutional or professional clients (excluding high-net-worth retail clients having opted out to be treated as professionals) will possibly not need to be affiliated to an ombudsman anymore. The new law is however still pending final approval. A practical approach has been agreed with FINMA that the supervisory authority will not enforce the affiliation requirement in the period from 26 December 2020 to 31 January 2021.</p>
							
							<p>Please find below the link to the publication on the Federal Department of Finance website:</p>
							
							<p>Limitation on ombudsman affiliation requirement.</p>
							
							<p>Clients of Financial Service Providers subject to FinSA</p>

							<p>You do not have to affiliate yourself with us, it is your financial service provider subject to LSFin who must affiliate itself with one of the recognized ombudsman / mediation organ. If you are facing a dispute with your financial service provider who is affiliated with us or willing to use our services, and you have already put your questions and complaints to it and attempted to find a settlement (art. 75(4)(b) FinSA), you may file a request for ombudsman support/mediation with us. You must send your request to our Secretariat at ombuds-fin@scai.swiss in either German, French, Italian or English, and pay a registration fee of CHF 100.- on the bank account indicated on our mediation request form. The costs of the mediation will be borne by your Financial Service Provider (art. 75(1) FinSA).</p>

							<p>Note that the dispute must not already have been the object of another mediation between the same parties, and that no other mediation/conciliation/ombudsperson or –entity, or arbitral court or an administrative entity can have been seized with the same subject matter between the same parties(art. 75(4)(c-d) FinSA).</p>

							<p>Your questions and complaint must neither concern questions of general business and fee policy nor abstract business and legal questions.</p>						
